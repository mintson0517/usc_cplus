#include <iostream>
using namespace std;

int main() {
    int a, b;
    cin >> a >> b;
    cout << "The decimal integer of the 1st variable is " << a << ".\n";
    cout << "The octal integer of the 1st variable is " << oct << a << ".\n";
    cout << "The hexadecimal integer of the 1st variable is " << hex << a << ".\n";
    cout << "The decimal integer of the 2nd variable is " << b << ".\n";
    cout << "The octal integer of the 2nd variable is " << oct << b << ".\n";
    cout << "The hexadecimal integer of the 2nd variable is " << hex << b << ".\n";
    return 0;
}
